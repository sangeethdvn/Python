a = int(input("Enter a number:  "))

b = int(input("Enter another number: "))

if (a % 2 == 0) and (b % 2 == 0):

    print("Both numbers are Even!!!!!")
 
elif (a % 2 != 0) and (b % 2 != 0):

    print("Both numbers are Odd!!!!!")

else:

    print("One of the number must be odd and another must be even!")
    
    