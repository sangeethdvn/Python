"""
Create a class calculator

methods: Addition, Subtraction

"""

class Calculator:
    def addition(self,a,b):

        print(a + b)

    def subtraction(self,a,b):

        print(a - b)


    def multiplication(self,a,b):

        print(a * b)

    def division(self,a,b):

        print(a / b)

obj1 = Calculator()

obj2 = Calculator()

obj3 = Calculator()

obj1.addition(109,2)

obj2.division(110,2)

obj3.multiplication(2,2)




    


